def substract_function (number_one, number_two):
    result=number_one-number_two
    return result

substraction= substract_function(10,2)
print(substraction)


def multiply_function (number_one,number_two,number_three):
    result=number_one*number_two*number_three
    return result
    
multiplication= multiply_function (2,3,4)
print(multiplication)
